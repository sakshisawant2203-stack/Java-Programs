package java_practice;
public class SwitchInhance {
    public static void main(String[] args) {
        String var = "mummy";
        switch (var) {
            case"sakshi"-> {
                System.out.println("you are going to get job:");
            }
            case"omkar"-> {
                System.out.println("you are going to become very sucessful as soon:");
            }
            case"mummy"-> {
                System.out.println("you are going to become very satisfied and happy as soon:");
            }
            case"pappa"-> {
                System.out.println("you are going to live very joyful and tensionfree life as soon:");
            }
        }
    }
}


